
number1 = 13
number2 = 14

print('PLUSE : ' ,number1 + number2)
print('MINUSE : ' ,number1 - number2)
print('ZARB : ' ,number1 * number2)
print('Taghsim : ' ,number1 / number2)
print('Bagimandeh : ' ,number1 % number2)
print('Tavan : ' ,number1 ** number2)

# زمانی که به این صورت تقسیم را انجام دهیم یعنی میگوییم فقط قسمت صحیح ان را میخواهیم

print('Taghsim : ' ,number1 // number2)

# تقدم اعداد
# 1- ( )  => پرانتز
# 2- ** => توان
# 3- +X or -X => مثبت و منفی بودن اعداد
# 4- * or  / or // or % => ضرب و تقسیم
# 4- + - => جمع و منهی

print(9 // 2)